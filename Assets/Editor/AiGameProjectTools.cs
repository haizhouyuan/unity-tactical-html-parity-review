    [MenuItem("AI Tools/Validate Project Setup")]
    public static void ValidateProjectSetup()
    {
        var missing = 0;

        foreach (var folder in RequiredFolders)
        {
            if (AssetDatabase.IsValidFolder(folder))
            {
                Debug.Log("[AI Tools] OK folder: " + folder);
            }
            else
            {
                missing++;
                Debug.LogWarning("[AI Tools] Missing folder: " + folder);
            }
        }

        var hasAgents = File.Exists(Path.Combine(Directory.GetCurrentDirectory(), "AGENTS.md"));
        var hasReadme = File.Exists(Path.Combine(Directory.GetCurrentDirectory(), "README.md"));
        var hasGitignore = File.Exists(Path.Combine(Directory.GetCurrentDirectory(), ".gitignore"));

        Debug.Log("[AI Tools] AGENTS.md: " + (hasAgents ? "present" : "missing"));
        Debug.Log("[AI Tools] README.md: " + (hasReadme ? "present" : "missing"));
        Debug.Log("[AI Tools] .gitignore: " + (hasGitignore ? "present" : "missing"));
        Debug.Log("[AI Tools] Project setup validation complete. Missing folders: " + missing);
    }

    [MenuItem("AI Tools/Create Star Collector Prototype")]
    public static void CreateStarCollectorPrototype()
    {
        EnsureFolders();

        var materials = CreatePrototypeMaterials();
        var playerPrefab = CreatePlayerPrefab(materials["Player"]);
        var starPrefab = CreateStarPrefab(materials["Star"]);

        var scene = EditorSceneManager.NewScene(NewSceneSetup.EmptyScene, NewSceneMode.Single);
        scene.name = "Main";

        CreateGround(materials["Ground"]);
        CreateLights();

        var manager = new GameObject("Game Manager").AddComponent<StarCollectorGameManager>();
        var player = (GameObject)PrefabUtility.InstantiatePrefab(playerPrefab);
        player.name = "Player";
        player.transform.position = new Vector3(0f, 1.05f, -5f);

        var camera = CreateCamera(player.transform);
        CreateUi(manager);
        CreateStars(starPrefab, manager);

        Selection.activeGameObject = player;
        EditorSceneManager.SaveScene(scene, MainScenePath);
        WriteProjectReport();

        Debug.Log("[AI Tools] Star Collector prototype created at " + MainScenePath + ". Press Play to test it.");
        Debug.Log("[AI Tools] Player uses WASD or arrow keys. Collect all " + StarCount + " stars to win.");
    }

